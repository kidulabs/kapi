//! Typed client wrapper for the kapi API server.
//!
//! Provides [`TypedClient<T>`] for type-safe CRUD operations on resources.

use std::marker::PhantomData;

use serde::{Serialize, de::DeserializeOwned};

use kapi_core::{ListOptions, ObjectMeta, ResourceKey, StoredObject, SystemMetadata};

use crate::client::KapiClient;
use crate::error::ClientError;

/// Errors that can occur when using the typed client.
#[derive(Debug, thiserror::Error)]
pub enum TypedError {
    /// Error from the underlying HTTP client.
    #[error(transparent)]
    Client(#[from] ClientError),

    /// JSON serialization or deserialization failure.
    #[error("serialization error: {0}")]
    Serialization(#[from] serde_json::Error),
}

/// Trait for types that can be used with [`TypedClient`].
///
/// Implement this trait on wrapper structs generated by `kapibuild api generate`.
/// The trait provides the bridge between typed Rust structs and the raw
/// [`StoredObject`] format used by the kapi server.
pub trait TypedResource: Sized + Send + Sync + 'static {
    /// The spec type for this resource.
    type Spec: Serialize + DeserializeOwned;

    /// The status type for this resource.
    type Status: Serialize + DeserializeOwned;

    /// Returns the [`ResourceKey`] identifying this resource type.
    fn key() -> ResourceKey;

    /// Constructs a resource from its component parts.
    fn from_parts(
        metadata: ObjectMeta,
        system: SystemMetadata,
        spec: Self::Spec,
        status: Option<Self::Status>,
    ) -> Self;

    /// Returns a reference to the resource's metadata.
    fn metadata(&self) -> &ObjectMeta;

    /// Returns a reference to the resource's system metadata.
    fn system(&self) -> &SystemMetadata;

    /// Returns a reference to the resource's spec.
    fn spec(&self) -> &Self::Spec;

    /// Returns a reference to the resource's status, if present.
    fn status(&self) -> Option<&Self::Status>;
}

/// A type-safe client for CRUD operations on kapi resources.
///
/// Wraps [`KapiClient`] and provides typed operations for any resource type
/// that implements [`TypedResource`].
///
/// # Example
///
/// ```ignore
/// let client = KapiClient::new("http://localhost:8080")?;
/// let typed = TypedClient::<Widget>::new(client);
///
/// // Create a new widget
/// let meta = ObjectMeta { name: "my-widget".into(), ..Default::default() };
/// let spec = WidgetSpec { color: "red".into() };
/// let widget = typed.create(Some("default"), &meta, &spec).await?;
///
/// // Get the widget back
/// let fetched = typed.get(Some("default"), "my-widget").await?;
/// ```
#[derive(Debug, Clone)]
pub struct TypedClient<T: TypedResource> {
    client: KapiClient,
    _marker: PhantomData<T>,
}

impl<T: TypedResource> TypedClient<T> {
    /// Creates a new typed client wrapping the given [`KapiClient`].
    pub fn new(client: KapiClient) -> Self {
        Self { client, _marker: PhantomData }
    }

    /// Returns a reference to the underlying [`KapiClient`].
    pub fn inner(&self) -> &KapiClient {
        &self.client
    }

    /// Creates a new resource.
    ///
    /// Extracts metadata and spec from the typed resource, serializes them to
    /// JSON, and sends them to the server. The server assigns system metadata
    /// and returns the created resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Client`] for HTTP/API errors.
    /// Returns [`TypedError::Serialization`] if spec serialization fails.
    pub async fn create(&self, namespace: Option<&str>, resource: &T) -> Result<T, TypedError> {
        let key = T::key();
        let meta = resource.metadata();
        let spec_value = serde_json::to_value(resource.spec())?;
        let stored = self.client.create(&key, namespace, meta, &spec_value).await?;
        Self::stored_to_typed(stored)
    }

    /// Retrieves a resource by name.
    ///
    /// Fetches the [`StoredObject`] from the server and deserializes it into
    /// the typed resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Client`] for HTTP/API errors.
    /// Returns [`TypedError::Serialization`] if deserialization fails.
    pub async fn get(&self, namespace: Option<&str>, name: &str) -> Result<T, TypedError> {
        let key = T::key();
        let stored = self.client.get(&key, namespace, name).await?;
        Self::stored_to_typed(stored)
    }

    /// Updates an existing resource.
    ///
    /// Serializes the typed resource to a [`StoredObject`] and sends it to the
    /// server. Returns the updated resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Client`] for HTTP/API errors.
    /// Returns [`TypedError::Serialization`] if serialization fails.
    pub async fn update(&self, namespace: Option<&str>, resource: &T) -> Result<T, TypedError> {
        let stored = Self::typed_to_stored(resource)?;
        let updated = self.client.update(namespace, &stored).await?;
        Self::stored_to_typed(updated)
    }

    /// Deletes a resource by name.
    ///
    /// Returns the deleted resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Client`] for HTTP/API errors.
    /// Returns [`TypedError::Serialization`] if deserialization fails.
    pub async fn delete(&self, namespace: Option<&str>, name: &str) -> Result<T, TypedError> {
        let key = T::key();
        let stored = self.client.delete(&key, namespace, name).await?;
        Self::stored_to_typed(stored)
    }

    /// Lists resources of this type.
    ///
    /// Fetches all resources (optionally filtered by namespace and list options)
    /// and deserializes each into the typed resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Client`] for HTTP/API errors.
    /// Returns [`TypedError::Serialization`] if any item fails to deserialize.
    pub async fn list(
        &self,
        namespace: Option<&str>,
        opts: &ListOptions,
    ) -> Result<Vec<T>, TypedError> {
        let key = T::key();
        let response = self.client.list(&key, namespace, opts).await?;
        response.items.into_iter().map(Self::stored_to_typed).collect()
    }

    // ------------------------------------------------------------------
    // Conversion helpers
    // ------------------------------------------------------------------

    /// Converts a [`StoredObject`] into the typed resource.
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Serialization`] if the spec or status fields
    /// cannot be deserialized into their typed counterparts.
    fn stored_to_typed(stored: StoredObject) -> Result<T, TypedError> {
        let spec: T::Spec = serde_json::from_value(stored.spec)?;
        let status: Option<T::Status> = match stored.status {
            Some(value) => Some(serde_json::from_value(value)?),
            None => None,
        };
        Ok(T::from_parts(stored.metadata, stored.system, spec, status))
    }

    /// Converts a typed resource into a [`StoredObject`].
    ///
    /// # Errors
    ///
    /// Returns [`TypedError::Serialization`] if the spec or status fields
    /// cannot be serialized to JSON.
    fn typed_to_stored(resource: &T) -> Result<StoredObject, TypedError> {
        let key = T::key();
        let metadata = resource.metadata().clone();
        let system = resource.system().clone();
        let spec = serde_json::to_value(resource.spec())?;
        let status = match resource.status() {
            Some(s) => Some(serde_json::to_value(s)?),
            None => None,
        };
        Ok(StoredObject { key, metadata, system, spec, status })
    }
}

