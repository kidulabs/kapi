#[cfg(test)]
mod tests {
    use super::*;
    use serde::{Deserialize, Serialize};
    use std::collections::HashMap;

    // Mock resource types for testing
    #[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
    #[serde(rename_all = "camelCase")]
    struct TestSpec {
        color: String,
        size: u32,
    }

    #[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
    #[serde(rename_all = "camelCase")]
    struct TestStatus {
        ready: bool,
        count: u32,
    }

    #[derive(Debug, Clone, Serialize, Deserialize)]
    #[serde(rename_all = "camelCase")]
    struct TestResource {
        metadata: ObjectMeta,
        system: SystemMetadata,
        spec: TestSpec,
        #[serde(skip_serializing_if = "Option::is_none")]
        status: Option<TestStatus>,
    }

    impl TypedResource for TestResource {
        type Spec = TestSpec;
        type Status = TestStatus;

        fn key() -> ResourceKey {
            ResourceKey {
                group: "test.io".to_string(),
                version: "v1".to_string(),
                kind: "TestResource".to_string(),
            }
        }

        fn from_parts(
            metadata: ObjectMeta,
            system: SystemMetadata,
            spec: Self::Spec,
            status: Option<Self::Status>,
        ) -> Self {
            Self { metadata, system, spec, status }
        }

        fn metadata(&self) -> &ObjectMeta {
            &self.metadata
        }

        fn system(&self) -> &SystemMetadata {
            &self.system
        }

        fn spec(&self) -> &Self::Spec {
            &self.spec
        }

        fn status(&self) -> Option<&Self::Status> {
            self.status.as_ref()
        }
    }

    fn test_metadata(name: &str) -> ObjectMeta {
        ObjectMeta {
            name: name.to_string(),
            namespace: Some("default".to_string()),
            labels: HashMap::new(),
            annotations: HashMap::new(),
            finalizers: Vec::new(),
        }
    }

    fn test_stored_object(
        name: &str,
        spec: serde_json::Value,
        status: Option<serde_json::Value>,
    ) -> StoredObject {
        StoredObject {
            key: TestResource::key(),
            metadata: test_metadata(name),
            system: SystemMetadata::initial(),
            spec,
            status,
        }
    }

    // --- Conversion tests ---

    #[test]
    fn stored_to_typed_with_status() {
        let spec = serde_json::json!({
            "color": "red",
            "size": 42
        });
        let status = serde_json::json!({
            "ready": true,
            "count": 5
        });
        let stored = test_stored_object("test-1", spec, Some(status));

        let typed = TypedClient::<TestResource>::stored_to_typed(stored).unwrap();

        assert_eq!(typed.metadata.name, "test-1");
        assert_eq!(typed.spec.color, "red");
        assert_eq!(typed.spec.size, 42);
        assert!(typed.status.is_some());
        assert!(typed.status.as_ref().unwrap().ready);
        assert_eq!(typed.status.as_ref().unwrap().count, 5);
    }

    #[test]
    fn stored_to_typed_without_status() {
        let spec = serde_json::json!({
            "color": "blue",
            "size": 100
        });
        let stored = test_stored_object("test-2", spec, None);

        let typed = TypedClient::<TestResource>::stored_to_typed(stored).unwrap();

        assert_eq!(typed.metadata.name, "test-2");
        assert_eq!(typed.spec.color, "blue");
        assert_eq!(typed.spec.size, 100);
        assert!(typed.status.is_none());
    }

    #[test]
    fn stored_to_typed_invalid_spec() {
        let spec = serde_json::json!({
            "color": "green"
            // missing "size" field
        });
        let stored = test_stored_object("test-3", spec, None);

        let result = TypedClient::<TestResource>::stored_to_typed(stored);
        assert!(result.is_err());
        assert!(matches!(result.unwrap_err(), TypedError::Serialization(_)));
    }

    #[test]
    fn typed_to_stored_with_status() {
        let resource = TestResource {
            metadata: test_metadata("test-4"),
            system: SystemMetadata::initial(),
            spec: TestSpec { color: "yellow".to_string(), size: 200 },
            status: Some(TestStatus { ready: false, count: 10 }),
        };

        let stored = TypedClient::<TestResource>::typed_to_stored(&resource).unwrap();

        assert_eq!(stored.metadata.name, "test-4");
        assert_eq!(stored.spec["color"], "yellow");
        assert_eq!(stored.spec["size"], 200);
        assert!(stored.status.is_some());
        assert_eq!(stored.status.as_ref().unwrap()["ready"], false);
        assert_eq!(stored.status.as_ref().unwrap()["count"], 10);
    }

    #[test]
    fn typed_to_stored_without_status() {
        let resource = TestResource {
            metadata: test_metadata("test-5"),
            system: SystemMetadata::initial(),
            spec: TestSpec { color: "purple".to_string(), size: 50 },
            status: None,
        };

        let stored = TypedClient::<TestResource>::typed_to_stored(&resource).unwrap();

        assert_eq!(stored.metadata.name, "test-5");
        assert_eq!(stored.spec["color"], "purple");
        assert_eq!(stored.spec["size"], 50);
        assert!(stored.status.is_none());
    }

    #[test]
    fn roundtrip_conversion() {
        let original = TestResource {
            metadata: test_metadata("test-6"),
            system: SystemMetadata::initial(),
            spec: TestSpec { color: "orange".to_string(), size: 75 },
            status: Some(TestStatus { ready: true, count: 3 }),
        };

        let stored = TypedClient::<TestResource>::typed_to_stored(&original).unwrap();
        let recovered = TypedClient::<TestResource>::stored_to_typed(stored).unwrap();

        assert_eq!(original.metadata.name, recovered.metadata.name);
        assert_eq!(original.metadata.namespace, recovered.metadata.namespace);
        assert_eq!(original.system.resource_version, recovered.system.resource_version);
        assert_eq!(original.system.generation, recovered.system.generation);
        assert_eq!(original.spec.color, recovered.spec.color);
        assert_eq!(original.spec.size, recovered.spec.size);
        assert_eq!(original.status.is_some(), recovered.status.is_some());
        if let (Some(orig), Some(rec)) = (original.status.as_ref(), recovered.status.as_ref()) {
            assert_eq!(orig.ready, rec.ready);
            assert_eq!(orig.count, rec.count);
        }
    }

    // --- Resource key tests ---

    #[test]
    fn resource_key_returns_correct_key() {
        let key = TestResource::key();
        assert_eq!(key.group, "test.io");
        assert_eq!(key.version, "v1");
        assert_eq!(key.kind, "TestResource");
    }

    // --- TypedClient construction tests ---

    #[test]
    fn typed_client_new() {
        let client = KapiClient::new("http://localhost:8080").unwrap();
        let typed = TypedClient::<TestResource>::new(client.clone());
        // Verify we can access the inner client
        let inner = typed.inner();
        assert_eq!(
            inner.build_url(&TestResource::key(), None, ""),
            "http://localhost:8080/apis/test.io/v1/TestResource"
        );
    }

    // --- Error conversion tests ---

    #[test]
    fn typed_error_from_client_error() {
        let client_err = ClientError::ApiError {
            status: 404,
            code: "NotFound".to_string(),
            message: "resource not found".to_string(),
        };
        let typed_err: TypedError = client_err.into();
        assert!(matches!(typed_err, TypedError::Client(_)));
        let msg = format!("{}", typed_err);
        assert!(msg.contains("404"));
        assert!(msg.contains("NotFound"));
    }

    #[test]
    fn typed_error_from_serialization_error() {
        let serde_err = serde_json::from_str::<TestSpec>("invalid json").unwrap_err();
        let typed_err: TypedError = serde_err.into();
        assert!(matches!(typed_err, TypedError::Serialization(_)));
        let msg = format!("{}", typed_err);
        assert!(msg.contains("serialization error"));
    }
}
